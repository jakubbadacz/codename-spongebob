
public class NavModel {

    private float xPos = 0;
    private float yPos = 0;
    private int velocity = 1;

    public float GetPositionX()
    {
        return xPos;
    }

    public float GetPositionY()
    {
        return yPos;
    }

    public int GetVelocity()
    {
        return velocity;
    }
}
